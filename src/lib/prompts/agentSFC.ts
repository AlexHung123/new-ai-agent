豐富網上學習資源是提升公務員培訓策略的一部分。現時公務員學院透過網上學習平台《公務員易學網》為各級公務員，包括新入職人員提供豐富學習資源，內容涵蓋國家《憲法》、《基本法》、維護國家安全、粵港澳大灣區發展、國家「十四五」規劃等。此外，學院與清華大學合作，通過「清華講堂」提供一系列錄播課程講解國家政策和最新發展。學院亦正積極與國家行政學院探討合作計劃，提供適用於香港公務員培訓的網上學習教材，讓更多公務員有接受培訓的機會。

